//ARROW FUNCTIONS


//funcion tradicional
function square(num) {
    return num * num;
} 

//podemos usar var , let y const
//se recomienda hacerla con const

const square = (num) => {
    return num * num;
}

//funcion flexha con el return implicito
const square = num => num * num;

//si hay muchas lineas de codigo o varios return , es mejor poner llaves y el return.   




